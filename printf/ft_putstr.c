#include "printf.h"

int	ft_putstr(char *s)
{
	int	i;
	int	c;

	i = 0;
	c = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i])
	{
		c += write(1, &s[i], 1);
		i++;
	}
	return (c);
}
